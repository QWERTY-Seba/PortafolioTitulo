    public void RellenarTabla(){
        jTable2.setModel(new DefaultTableModel(null,columnas_contrato));
        ResetBotones();
        lc = DAO.Usuario_DAO.ListarCliente(-1);
        Object[][] datos = new Object[lc.size()][6];

        int i = 0;
        for(Cliente s : lc){
            datos[i][0] = s.getId_cliente();
            datos[i][1] = s.getRut();
            datos[i][2] = s.getNombres();
            datos[i][3] = s.getApellidos();
            datos[i][4] = s.getCorreo();
            datos[i][5] = s.getC_role();
            i++;
            }
        jTable1.setModel(new DefaultTableModel(datos,columnas_cliente));
    }

    public void RellenarTablaContrato(Cliente c){
        
        //String[] Columnas = {"id_especificacion","Producto","Cantidad","Calidad"};
        Object[][] celdas = new Object[c.getContratos().size()][6];
        int i = 0;
        for(Contrato ec : c.getContratos()){
            celdas[i][0] = ec.getId_contrato();
            celdas[i][1] = ec.getFecha_creacion();
            celdas[i][2] = ec.getFecha_termino();
            celdas[i][3] = ec.getTipo_contrato();
            celdas[i][4] = ec.getDetalle();
            celdas[i][5] = ec.getEstado();
            i++;
        }
        jTable2.setModel(new DefaultTableModel(celdas, columnas_contrato));
    }
    
    public void Rellenar(Contrato con){
        String[] Columnas = {"id_especificacion","Producto","Cantidad","Calidad"};
        Object[][] celdas = new Object[1][4];
        
            celdas[0][0] = con.getId_contrato();
            celdas[0][1] = con.getId_producto();
            celdas[0][2] = con.getCantidad();
            celdas[0][3] = con.getCalidad();
        jTable3.setModel(new DefaultTableModel(celdas, Columnas));        
    }
