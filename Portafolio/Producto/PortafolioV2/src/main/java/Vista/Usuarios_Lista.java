    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        Integer id = jTable2.getSelectedRow();
        if(id != -1 ){
            if(jTable2.getValueAt(id, 5).equals("Activo")){
                jButton3.setEnabled(true);
                jButton7.setEnabled(true);
                jButton2.setEnabled(true);
            }else{
               jButton3.setEnabled(false);
               jButton7.setEnabled(false); 
               jButton2.setEnabled(false); 
            }
            if(jTable2.getValueAt(id, 3).equals(Clases.Estados.Tipos_Contrato.Fruta.name())){
                Rellenar(ContratoCliente((Integer) jTable2.getValueAt(id, 0)));
            }else{
                jTable3.setModel(new DefaultTableModel());
            }
        }
    }//GEN-LAST:event_jTable2MouseClicked

