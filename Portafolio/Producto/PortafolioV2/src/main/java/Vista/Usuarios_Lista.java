    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        Integer index = jTable1.getSelectedRow();
        if(index == -1){
            //jButton5.setEnabled(false);
            BVerDetalle.setEnabled(false);
            jButton4.setEnabled(false);
            jButton6.setEnabled(false);
        }else{
            //jButton5.setEnabled(true);
            BVerDetalle.setEnabled(true);
            jButton4.setEnabled(true);
            jButton6.setEnabled(true);
            if(jTable1.getValueAt(index, 5) == null){//.equals("Cliente_Externo")){
                jButton8.setEnabled(false);
            }else if(jTable1.getValueAt(index, 5).equals("Cliente_Externo")){
                jButton8.setEnabled(true);
            }
        }

    }//GEN-LAST:event_jTable1MouseClicked

    public Contrato ContratoCliente(Integer id){
        return c.getContratos().stream().filter(s -> s.getId_contrato() == id).findFirst().orElse(null);
    }
    public Cliente EncontrarClienteLocal(Integer id){
        return lc.stream().filter(s -> s.getId_cliente() == id).findFirst().orElse(null);
    }
