    public void RellenarPagos(){
        List<Pago> lista = DAO.Usuario_DAO.ListarPagos();
        Object[][] datos = new Object[lista.size()][9];
        int i = 0;
        for(Pago pago : lista){
            datos[i][0] = pago.getId_pago();
            datos[i][1] = pago.getCurrency();
            datos[i][2] = pago.getValue();
            datos[i][3] = pago.getId_pedido();
            datos[i][4] = pago.getNombre();
            datos[i][5] = pago.getPais();
            datos[i][6] = pago.getFecha_pago();
            datos[i][7] = pago.getId_pago_in();
            i++;
        }         
        jTable1.setModel(new DefaultTableModel(datos,columnas));
   }
    
    
    
