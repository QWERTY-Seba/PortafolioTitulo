    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Integer index = jTable1.getSelectedRow();
        if(index != -1){
            Subasta subasta = new Subasta();
            subasta.setFecha_termino(jTextField1.getText());
            subasta.setTipo_subasta(jComboBox2.getSelectedItem().toString());
            subasta.setPrecio_por_kg(Integer.valueOf(jTextField3.getText()));
            
            Produccion p = new Produccion();
            p.setId_produccion((Integer)jTable1.getValueAt(index, 0) );
            subasta.setProduccion(p);
            DAO.Subasta_DAO.CrearSubastaVenta(subasta);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

