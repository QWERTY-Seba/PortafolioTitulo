    public void Rellenar(){
        Lista_Producciones = DAO.Usuario_DAO.ListarProduccionesParaVenta();
        String[] columnas = {"id_produccion","id_producto","fecha_cosecha","Calidad","Cantidad_Restante"};
        Object[][] datos = new Object[Lista_Producciones.size()][columnas.length];
        int i = 0;
        for(Produccion p : Lista_Producciones){
            datos[i][0] = p.getId_produccion();
            datos[i][1] = p.getId_producto();
            datos[i][2] = p.getFecha_cosecha();
            datos[i][3] = p.getCalidad();
            datos[i][4] = p.getCantidadRestante();
            i++;
        }
        jTable1.setModel(new DefaultTableModel(datos, columnas));
    }    
    
